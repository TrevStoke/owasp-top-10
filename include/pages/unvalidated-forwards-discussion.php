<h2>Discussion</h2>
<p>Websites often make use of redirection. It's a convenient way of chaining actions.
These re-directions garner a lot of trust from the viewer, proportionate to the subjective
    trust level of the primary domain.</p>
<p>Unvalidated re-direction opens the door to potential mis-use as the target site is
    often able to assume the relative trust held by the user. This could lead the user
to a new site that is aesthetically similar. If the user does not notice, they may
inadvertently expose their authentication credentials to a facade.</p>